// 1. Написать функцию, преобразующую число в объект. Передавая на вход число от 0
// до 999, мы должны получить на выходе объект, в котором в соответствующих
// свойствах описаны единицы, десятки и сотни. Например, для числа 245 мы должны
// получить следующий объект: {‘единицы’: 5, ‘десятки’: 4, ‘сотни’: 2}. Если число
// превышает 999, необходимо выдать соответствующее сообщение с помощью
// console.log и вернуть пустой объект.

function get_object(number) {
    if (number >= 0 & number <= 999) {
        let str = number.toString()
        return {'единицы': str[2], 'десятки': str[1], 'сотни': str[0]}
    }
    else {
        console.log(`Число ${number} не попадает в интервал от 0 до 999`)
        return {}
    }
}

console.log(get_object(345))
console.log(get_object(3456))
